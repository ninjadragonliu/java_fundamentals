package labs_examples.objects_classes_methods.labs.oop.B_polymorphism;

public class Addition {
    public int add(int a, int b) {
        return a + b;
    }
    public int add(int a, int b, int c) {
        return a + b + c;
    }
    public double add(double a, double b) {
        return a + b;
    }
}
